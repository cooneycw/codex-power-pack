def _get_health_url(config: CICDConfig, default_port: int) -> str:
    """Get health check URL from config or default."""
    if config.health.endpoints:
        return config.health.endpoints[0].url
    return f"http://localhost:{default_port}/health"


def _dockerfile_python(
    info: FrameworkInfo,
    config: CICDConfig,
    base_image: str,
    runtime_image: str,
    ports: list[int],
    health_url: str,
) -> str:
    uses_uv = info.package_manager == PackageManager.UV
    lines: list[str] = []

    lines.append(f"FROM {base_image} AS builder")
    lines.append("")
    lines.append("WORKDIR /app")
    lines.append("")

    if uses_uv:
        lines.append("# Install uv")
        lines.append("COPY --from=ghcr.io/astral-sh/uv:latest /uv /usr/local/bin/uv")
        lines.append("")
        lines.append("# Install dependencies (cached layer)")
        lines.append("COPY pyproject.toml uv.lock* ./")
        lines.append("RUN uv sync --frozen --no-dev --no-install-project")
        lines.append("")
        lines.append("# Copy source and install project")
        lines.append("COPY . .")
        lines.append("RUN uv sync --frozen --no-dev")
    else:
        lines.append("# Install dependencies (cached layer)")
        lines.append("COPY requirements*.txt ./")
        lines.append("RUN pip install --no-cache-dir -r requirements.txt")
        lines.append("")
        lines.append("# Copy source")
        lines.append("COPY . .")

    lines.append("")
    lines.append(f"FROM {runtime_image}")
    lines.append("")
    lines.append("WORKDIR /app")
    lines.append("")
    lines.append("# Create non-root user")
    lines.append("RUN groupadd -r appuser && useradd -r -g appuser appuser")
    lines.append("")

    if uses_uv:
        lines.append("# Copy virtual environment from builder")
        lines.append("COPY --from=builder /app/.venv /app/.venv")
        lines.append("COPY --from=builder /app /app")
        lines.append("ENV PATH=\"/app/.venv/bin:$PATH\"")
    else:
        lines.append("# Copy installed packages and source from builder")
        lines.append("COPY --from=builder /usr/local/lib/python3.12 /usr/local/lib/python3.12")
        lines.append("COPY --from=builder /usr/local/bin /usr/local/bin")
        lines.append("COPY --from=builder /app /app")

    lines.append("")
    for port in ports:
        lines.append(f"EXPOSE {port}")
    lines.append("")
    lines.append("USER appuser")
    lines.append("")
    lines.append('HEALTHCHECK --interval=30s --timeout=5s --retries=3 \\')
    lines.append(f'  CMD curl -f {health_url} || exit 1')
    lines.append("")
    lines.append('CMD ["python", "-m", "uvicorn", "app:app", "--host", "0.0.0.0", "--port", "8000"]')
    lines.append("")
    return "\n".join(lines)


def _dockerfile_node(
    info: FrameworkInfo,
    config: CICDConfig,
    base_image: str,
    runtime_image: str,
    ports: list[int],
    health_url: str,
) -> str:
    uses_yarn = info.package_manager == PackageManager.YARN
    lines: list[str] = []

    lines.append(f"FROM {base_image} AS builder")
    lines.append("")
    lines.append("WORKDIR /app")
    lines.append("")
    lines.append("# Install dependencies (cached layer)")

    if uses_yarn:
        lines.append("COPY package.json yarn.lock ./")
        lines.append("RUN yarn install --frozen-lockfile --production=false")
    else:
        lines.append("COPY package.json package-lock.json* ./")
        lines.append("RUN npm ci")

    lines.append("")
    lines.append("# Copy source and build")
    lines.append("COPY . .")
    lines.append("RUN npm run build 2>/dev/null || true")
    lines.append("")
    lines.append(f"FROM {runtime_image}")
    lines.append("")
    lines.append("WORKDIR /app")
    lines.append("")
    lines.append("# Create non-root user")
    lines.append("RUN addgroup -S appuser && adduser -S appuser -G appuser")
    lines.append("")
    lines.append("# Copy production dependencies and build output")
    lines.append("COPY --from=builder /app/node_modules ./node_modules")
    lines.append("COPY --from=builder /app/dist ./dist 2>/dev/null || true")
    lines.append("COPY --from=builder /app/build ./build 2>/dev/null || true")
    lines.append("COPY --from=builder /app/package.json ./")
    lines.append("")

    for port in ports:
        lines.append(f"EXPOSE {port}")
    lines.append("")
    lines.append("USER appuser")
    lines.append("")
    lines.append('HEALTHCHECK --interval=30s --timeout=5s --retries=3 \\')
    lines.append(f'  CMD wget -qO- {health_url} || exit 1')
    lines.append("")
    lines.append('CMD ["node", "dist/index.js"]')
    lines.append("")
    return "\n".join(lines)


def _dockerfile_go(
    info: FrameworkInfo,
    config: CICDConfig,
    base_image: str,
    runtime_image: str,
    ports: list[int],
    health_url: str,
) -> str:
    lines: list[str] = []

    lines.append(f"FROM {base_image} AS builder")
    lines.append("")
    lines.append("WORKDIR /app")
    lines.append("")
    lines.append("# Download dependencies (cached layer)")
    lines.append("COPY go.mod go.sum ./")
    lines.append("RUN go mod download")
    lines.append("")
    lines.append("# Build static binary")
    lines.append("COPY . .")
    lines.append("RUN CGO_ENABLED=0 GOOS=linux go build -ldflags='-w -s' -o /app/server ./...")
    lines.append("")
    lines.append(f"FROM {runtime_image}")
    lines.append("")
    lines.append("# Copy binary from builder")
    lines.append("COPY --from=builder /app/server /server")
    lines.append("")
    for port in ports:
        lines.append(f"EXPOSE {port}")
    lines.append("")
    lines.append("# Run as non-root (distroless uses numeric UID)")
    lines.append("USER 65534")
    lines.append("")
    lines.append('ENTRYPOINT ["/server"]')
    lines.append("")
    return "\n".join(lines)


def _dockerfile_rust(
    info: FrameworkInfo,
    config: CICDConfig,
    base_image: str,
    runtime_image: str,
    ports: list[int],
    health_url: str,
) -> str:
    lines: list[str] = []

    lines.append(f"FROM {base_image} AS builder")
    lines.append("")
    lines.append("WORKDIR /app")
    lines.append("")
    lines.append("# Cache dependencies")
    lines.append("COPY Cargo.toml Cargo.lock ./")
    lines.append("RUN mkdir src && echo 'fn main() {}' > src/main.rs")
    lines.append("RUN cargo build --release && rm -rf src target/release/deps/app*")
    lines.append("")
    lines.append("# Build actual binary")
    lines.append("COPY . .")
    lines.append("RUN cargo build --release")
    lines.append("")
    lines.append(f"FROM {runtime_image}")
    lines.append("")
    lines.append("# Install curl for healthcheck")
    lines.append("RUN apt-get update && apt-get install -y --no-install-recommends curl && rm -rf /var/lib/apt/lists/*")
    lines.append("")
    lines.append("# Create non-root user")
    lines.append("RUN groupadd -r appuser && useradd -r -g appuser appuser")
    lines.append("")
    lines.append("# Copy binary from builder")
    lines.append("COPY --from=builder /app/target/release/app /usr/local/bin/app")
    lines.append("")
    for port in ports:
        lines.append(f"EXPOSE {port}")
    lines.append("")
    lines.append("USER appuser")
    lines.append("")
    lines.append('HEALTHCHECK --interval=30s --timeout=5s --retries=3 \\')
    lines.append(f'  CMD curl -f {health_url} || exit 1')
    lines.append("")
    lines.append('CMD ["app"]')
    lines.append("")
    return "\n".join(lines)


